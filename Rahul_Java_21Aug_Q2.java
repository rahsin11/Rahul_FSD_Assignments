class Rahul_Java_21Aug_Q2{
	public static void main(String[] args) {
		for(int i=35; i<=46; i++){
			if(i==40 || i==43){
				continue;
			}
			else if(i==38){
            System.out.println("Thirty Eight");
			}
			else if(i==45){
            System.out.println("Fourty Five");
			}
			else{
			System.out.println(i);
		    }
		}
	}
}